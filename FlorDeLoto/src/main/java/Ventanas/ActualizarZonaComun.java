    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        // TODO add your handling code here:
        String nombreZona = (String) campoZonaComun.getSelectedItem();
        String horaInicio = campoHoraInicio.getText();
        String horaFin = campoHoraFinal.getText();
        String disponibilidad = (String) campoDisponibilidad.getSelectedItem();

        // Crear una instancia de GestionCSV
        GestionCSV gestion = new GestionCSV();

        // Importar todas las zonas desde el archivo CSV para tener la lista actualizada
        gestion.importarZonas("RegistroZonasComunes.csv");

        // Buscar y eliminar la zona existente
        ZonaComun zonaExistente = gestion.encontrarZona(nombreZona);
        if (zonaExistente != null) {
            // Eliminar la zona existente de la lista
            gestion.eliminarZona(zonaExistente);
        }

        // Registrar la nueva zona
        gestion.registrarZona(nombreZona, horaInicio, horaFin, disponibilidad);

    }//GEN-LAST:event_botonActualizarActionPerformed

