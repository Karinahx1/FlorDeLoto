    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // TODO add your handling code here:

        String nombreLugar = (String) campoZonaComun.getSelectedItem();

        // Crear una instancia de GestionCSV
        GestionCSV gestion = new GestionCSV();

        // Importar todas las zonas desde el archivo CSV
        gestion.importarZonas("RegistroZonasComunes.csv");

        // Buscar la zona seleccionada en la lista
        ZonaComun zonaComun = gestion.encontrarZona(nombreLugar);

        if (zonaComun != null) {
            // Mostrar los datos encontrados en los campos de la interfaz
            campoHoraInicio.setText(zonaComun.getHoraInicio());
            campoHoraFinal.setText(zonaComun.getHoraFinal());
            campoDisponibilidad.setSelectedItem(zonaComun.getDisponibilidad());
        } else {
            // Mostrar un mensaje de error si no se encuentra la zona
            JOptionPane.showMessageDialog(this,
                "Espacio no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

